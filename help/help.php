        cPanel</h3>
    <hr>

    <img class="img-panel-info" src="<?php echo plugin_dir_url(__FILE__) . '/img/cPanel-file-manager.png' ?>" alt=""/>
    <p style="padding-top:10px;">
    </p>

    <p>When you log in on your <b>cPanel</b> host account search type <b>File Manager</b> like on the picture.</p>

    <p>Click on the File Manager <b>icon</b> and in the new web browser tab you see the <b>File Manager</b> window and
        all your hosting folders and files.</p>

    <p>Go to directory <b>public_html</b> and found file <b>wp-config.php</b> click the right mouse to see a menu, after
        that click <b>edit</b> with the left click, if you get some popup window just click edit.</p>

    <p>When you see the file editor, look to find <b style="border: 1px solid; padding: 3px;">define(
            'DISALLOW_FILE_EDIT', true );</b> and change from <b style="color: #0000ff;">true</b> to <b
                style="color: #0000ff;">false</b>.</p>

    <p>Click on the top right button <b>Save Changes</b> and then click on the <b>Close</b> button.</p>

    <p>Now you can edit your style on the <b>Manage Styles</b> button.</p>
